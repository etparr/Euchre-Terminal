public class TextUtil {

    /**
     * Returns large block of blank lines; obscures prior text.
     */
    public static void textBrick(){
        System.out.println("\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n");
    }

    /**
     * Returns text line
     * "*~~~~~~~~~~~w~~~~~~~~~~~~~~~~*"
     */
    public static void textLine() {
        System.out.println("*~~~~~~~~~~~~~~~~~~~~~~~~~~~*");
    }
}
